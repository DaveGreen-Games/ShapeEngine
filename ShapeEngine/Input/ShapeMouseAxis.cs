namespace ShapeEngine.Input;

/// <summary>
/// Represents the axes of mouse movement.
/// </summary>
public enum ShapeMouseAxis
{
    /// <summary>
    /// The horizontal mouse axis (X-axis).
    /// </summary>
    HORIZONTAL = 0,
    /// <summary>
    /// The vertical mouse axis (Y-axis).
    /// </summary>
    VERTICAL = 1,
}