namespace ShapeEngine.Input;

/// <summary>
/// Specifies the axis of the mouse wheel movement.
/// </summary>
public enum ShapeMouseWheelAxis
{
    /// <summary>
    /// Horizontal axis (typically left/right scrolling).
    /// </summary>
    HORIZONTAL = 0,

    /// <summary>
    /// Vertical axis (typically up/down scrolling).
    /// </summary>
    VERTICAL = 1,
}